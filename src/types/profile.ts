export interface ProfileData {
  // Step 1: Basic Info
  name: string;
  description: string;

  // Step 2: Location
  location: {
    country: string; // ISO 3166-1 alpha-2 code (e.g., "GE", "IL")
    city: string;
  };
  clientCountries: string[]; // ISO 3166-1 alpha-2 codes
  visibleForCountries: string[]; // ISO 3166-1 alpha-2 codes for backend

  // Step 3: Services
  serviceType: string;
  servicesList: string[];

  // Step 4: Photos
  photos: string[];

  // Step 5: Contact
  contactInfo: {
    phoneCountryCode?: string;
    phoneNumber?: string;
    telegram?: string;
    email?: string;
    exposeTelegram?: boolean;
    exposeWhatsApp?: boolean;
  };

  // Step 6: Pricing
  pricing: {
    currency: string;
    rates: {
      [duration: string]: {
        incall?: number;
        outcall?: number;
      };
    };
  };
}

export interface ProfileState {
  data: Partial<ProfileData>;
  currentStep: number;
  completedSteps: Set<number>;
  isLoading: boolean;
}

